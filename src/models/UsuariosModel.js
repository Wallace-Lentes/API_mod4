class UsuariosModel{
    constructor(id, nome, email, senha, telefone, cpf, cep, numero){
        this.id = id
        this.nome = nome
        this.email = email
        this.senha = senha
        this.telefone = telefone
        this.cpf = cpf
        this.cep = cep
        this.numero = numero
    }
}
export default UsuariosModel